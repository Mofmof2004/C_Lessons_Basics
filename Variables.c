int Variable()
{
    // Variable made from characters
    // [] = multiple characters
    char Name [] = "value";

    // Variable made of integer
    int Number = 35;

    //%s = add string(variable) on the print function + "," and name of the variable which will replace the %s
    printf("information %s\n", Name);
    //%d = add integer(variable) on the print function + "," and name of the variable which will replace the %d
    printf("information %d\n", Number);
    return;
}
